drop policy if exists "agent_foundation_agents_select_member" on public.agents;
create policy "agent_foundation_agents_select_member"
on public.agents for select to authenticated
using (
  status <> 'deleted'
  and exists (
    select 1 from public.workspace_members wm
    where wm.workspace_id = agents.workspace_id and wm.user_id = auth.uid()
  )
);

drop policy if exists "agent_foundation_agents_write_admin" on public.agents;
create policy "agent_foundation_agents_write_admin"
on public.agents for all to authenticated
using (
  exists (
    select 1 from public.workspace_members wm
    where wm.workspace_id = agents.workspace_id
      and wm.user_id = auth.uid()
      and wm.role in ('owner', 'admin')
  )
)
with check (
  exists (
    select 1 from public.workspace_members wm
    where wm.workspace_id = agents.workspace_id
      and wm.user_id = auth.uid()
      and wm.role in ('owner', 'admin')
  )
);

