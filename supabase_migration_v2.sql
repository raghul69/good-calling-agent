-- RapidX Voice OS - Supabase SaaS schema and RLS
-- Run in Supabase SQL Editor before using the production dashboard.

create table if not exists public.call_logs (
    id bigint generated by default as identity primary key,
    created_at timestamptz not null default timezone('utc', now()),
    user_id uuid references auth.users(id) on delete set null,
    phone text not null default 'unknown',
    caller_name text default '',
    duration integer not null default 0,
    transcript text default '',
    summary text default '',
    recording_url text default '',
    sentiment text default 'unknown',
    estimated_cost_usd numeric default 0,
    call_date date,
    call_hour integer,
    call_day_of_week text default '',
    was_booked boolean default false,
    interrupt_count integer default 0
);

create table if not exists public.active_calls (
    room_id text primary key,
    user_id uuid references auth.users(id) on delete set null,
    phone text default 'unknown',
    caller_name text default '',
    status text default 'active',
    last_updated timestamptz default now()
);

create table if not exists public.call_transcripts (
    id bigint generated by default as identity primary key,
    created_at timestamptz default now(),
    user_id uuid references auth.users(id) on delete set null,
    call_room_id text,
    phone text default 'unknown',
    role text,
    content text
);

